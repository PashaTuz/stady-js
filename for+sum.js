// Починаємо з 0.
let sum = 0;
// Створити цикл for:
// створеня let i = 1 (першe числo - 1)
// умова: i <= 10 (продовжуємо, доки i не досягне 10 включно)
// далі: i++ (збільшуємо щьотчик на 1 на кожній ітерації)
for (let i = 1; i <= 10; i++) {
    // одаємо поточне значення i до загальної суми
    sum += i; 
    // інший варіант запису: sum = sum + i;
}

console.log("Сума чисел від 1 до 10 дорівнює:");
console.log(sum); // правильний результат: 55
